package com.ocean.java.string;

public class CharAtExample {

    public static void main(String args[]) {
        char ch;
        String s1 = "OCEAN";
        ch = s1.charAt(0);
        System.out.println(" Character at 0 : " + ch);
        ch = "OCEAN".charAt(3);
        System.out.println(" Character at 3 : " + ch);
    }
}