package com.gopinath.java.conditional;

public class OddEven {

    public static void main(String args[]) {
        int n;
        n = 33;
        if (n % 2 == 0) {
            System.out.println("Number " + n + " is Even Number.");
        } else {
            System.out.println("Number " + n + " is Odd Number.");
        }
    }
}